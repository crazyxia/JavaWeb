package com.javaWeb.bean;

public class StudentBean {
	private String name;
	private int count;

	public StudentBean() {
	}


	public String getName() {
		return name;
	}


	public void setName(String name) {
		this.name = name;
	}


	public int getCount() {
		return count;
	}

	public void setCount(int count) {
		this.count = count;
	}

	
}
